public class primitve {
    public static void main(String args[])
    {
     // These 4 are premitive data types?
        int studentRoll=123;
        double StudentMarks=34.76;
        char name='K';
        boolean isPerfect=true;

      //Here String is Reference types/Non-premitive data types
     // String:- String is a sequence of ordered elements.
        String Studentname="kumar gaurav";


        System.out.println(studentRoll);
        System.out.println(StudentMarks);
        System.out.println(isPerfect);
        System.out.println(name);
        System.out.println(Studentname);
    }
}
